"""
This program is to test if multiple assignment 
is available for strings too.

"""

#Multiple assignment for integers
x,y,z = 6,7,8
print(x,y,z)
print(z,x,y)

#Multiple assignment for strings
first_name,last_name = "Sijan","Shrestha"
print(first_name,last_name)
print(last_name,first_name)

"""
In conclusion,
multiple assignment is available for both numbers and strings.

"""