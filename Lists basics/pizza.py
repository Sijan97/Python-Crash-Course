#Exercise 4-1
#Creating a list and printing each name using for loop

pizzas = ['chicken', 'pepperoni', 'chese']
for pizza in pizzas:
	print(f"Today I would love to have a {pizza.title()} pizza.")
print("I really love pizza!")

